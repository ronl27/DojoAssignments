// CONDITIONALS
// if - else statements

var x = 15;
if (x < 10){
  console.log("x is less than 10");
} else if (x < 20){
  console.log('x is less than 20');
} else {
  console.log("x is greater than 20!");
}


// WHILE True (CONDITIONALS Cont.)

while(driving){
  // keep eyes on road
  // keep our hands on the wheel
  // obey speed limit signs
}

// these are conditions that continue while the driving is true. Once false, the condition ends.

// Example:
function countDown(number){
  while(number > 0){
    console.log(number);
    number --;
  }
}
countDown(10);
